package com.company;

public class CWH_04_literals {
    public static void main(String[] args) {

        //Literal --> A constant value which can be assigned to literal is called as a literal.
        byte age = 34;
        int age2 = 56;
        // class --> Integer, Byte
        short age3 = 56;
        long ageDino = 56666666666L; //5666666l or 5666666L
        char ch = 'A'; //character literal
        float f1 = 5.6f; //floating point literal
        double d1 = 4.66; //double literal 4.66D or d or nothing
        boolean a = true;

        String str = "Honey";


        System.out.println(age);
        System.out.println(str);

        /* Keywords :
        Keywords which are reserved and used by the Java Compiler.
        They cannot be used as an identifier.
        */
    }
}
